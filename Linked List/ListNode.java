public class ListNode {
	   int data;
	   ListNode next; //a reference pointing to the next node in the list
	   
	   public ListNode(int data, ListNode next) {
	      this.data = data;
	      this.next = next;
	    }
	    
	    public ListNode(int data) {
	       this.data = data;
	       this.next = null;
	    }
	}

